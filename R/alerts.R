   if(!is.null(emailParams[["greeting"]])) {
    greeting <- emailParams[["greeting"]]
   }
   if(!is.null(emailParams[["message"]])) {
    message <- emailParams[["message"]]
   }

    summEqAttach <- FALSE

    if(!is.null(data)) {
        summary  <- data[[1]]
        addendum <- emailHTMLTable(summary,
                                   provider=deployment,
                                   caption="",
                                   sourceType="API"
                                  )
        addendum <- stringr::str_replace_all(addendum,"&#60;","<")
        addendum <- stringr::str_replace_all(addendum,"&#62;",">")
              
        detail <- data[[length(data)]] 
        readr::write_csv(detail, file=dtPath)
        attachment <- dtPath

        summEqAttach <- isTRUE(dplyr::all_equal(summary,detail))
        
    }

    if(summEqAttach) {

      if(noBody) {
        addendum <- ""
      }
      else {
        attachment <- NULL
      }

    }

    ## Construct the content of the alert email:
    .UPDATETEXT <- list("GREETINGPLACEHOLDER"=greeting,
                        "EMAILBODYPLACEHOLDER"=message,
                        "CALLTOACTIONPLACEHOLDER"="Go to System",
