                       data=NULL,                    
                       emailParams,
                       dtPath="details.csv",
                       snd=Sys.Date(),               
                       wdw=c("00:00:00","23:59:00"), 
                       dte=NULL,                     
                       tz="Asia/Singapore",
                       noBody=FALSE,
                       ...
                      ) {

